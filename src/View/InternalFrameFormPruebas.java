    public void mostrarTabla(Citas[] citas,Clientes[] clientes) {
        SimpleDateFormat Fecha = new SimpleDateFormat("yyyyMMdd");
        DefaultTableModel model=new DefaultTableModel();
        model.addColumn("CÃ©dula");
        model.addColumn("NombreCompleto");
        model.addColumn("Fecha");
        model.addColumn("Hora");
        
        for(int x=0;x<citas.length;x++){
            try{
              String date=Fecha.format(citas[x].getFecha());
              model.addRow(new Object[]{citas[x].getCedula(),clientes[x].getNombre(),Fecha.parse(date),citas[x].getHora()});
            }catch(ParseException ex){
                JOptionPane.showMessageDialog(this, "Error en la fecha", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
        this.tabla.setModel(model);
    }
