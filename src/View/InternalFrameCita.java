    public void mostrarTodo(ArrayList<CitasDTO> lista) {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("idcita");
    model.addColumn("Cedula");
    model.addColumn("fecha");
    model.addColumn("hora");
    model.addColumn("estado");

    for (CitasDTO citas : lista) {
        Object[] row = {
            citas.getIdCita(),
            citas.getCedula(),
            citas.getFecha(),
            citas.getHora(),
            citas.isActiva() ? Boolean.TRUE : Boolean.FALSE
        };
        model.addRow(row);
    }
    this.TablaInfo.setModel(model);
  }
    
